package com.ese.kafka.user.infrastructure.event.consumer.specific;

import com.ese.kafka.autogenerated.events.user.UserEmailUpdatedEvent;
import com.ese.kafka.common.application.Mediator;
import com.ese.kafka.common.infrastructure.event.consumer.EventSpecificConsumer;
import com.ese.kafka.user.application.emailUpdated.UserEmailUpdatedCommand;
import com.ese.kafka.user.infrastructure.event.mapper.UserEventMapper;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.messaging.Message;
import org.springframework.stereotype.Component;

@Component
@Slf4j
@RequiredArgsConstructor
public class UserEmailUpdatedSpecificConsumer implements EventSpecificConsumer<UserEmailUpdatedEvent> {

    private final Mediator mediator;
    private final UserEventMapper userEventMapper;

    @Override
    public void accept(Message<UserEmailUpdatedEvent> genericRecordMessage) {
        log.info("UserEmailUpdatedSpecificConsumer message: {}", genericRecordMessage.toString());
        UserEmailUpdatedCommand command = userEventMapper.mapToUserEmailUpdatedCommand(genericRecordMessage.getPayload());
        mediator.dispatch(command);
    }

    @Override
    public String getSchema() {
        return UserEmailUpdatedEvent.getClassSchema().getFullName();
    }
}
